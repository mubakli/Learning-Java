public class Calculator {
    public float add( float a, float b){
        return a+b;
    }
    public float add( float a, float b, float c, float d){
        return a+b+c+d;
    }
    public int add(int a, int b){
        return a+b;
    }
    public int  add(int a, int b, int c, int d){
        return  a+b+c+d;
    }
}
